package com.hengyuan.hicash.entity.remote;

public class ContactsEntity {

	private String contacts_tel;

	private String contacts_name;

	private String contacts_type;

	public String getContacts_tel() {
		return contacts_tel;
	}

	public void setContacts_tel(String contacts_tel) {
		this.contacts_tel = contacts_tel;
	}

	public String getContacts_name() {
		return contacts_name;
	}

	public void setContacts_name(String contacts_name) {
		this.contacts_name = contacts_name;
	}

	public String getContacts_type() {
		return contacts_type;
	}

	public void setContacts_type(String contacts_type) {
		this.contacts_type = contacts_type;
	}

}
